use std::time::Duration;

use benimator::{Frame, SpriteSheetAnimation};
use bevy::prelude::*;
use bevy::sprite::Rect;
use bevy_loading::prelude::*;

pub const TILE_SIZE: f32 = 70.0;
pub struct SpriteAssets {
    pub texture: Handle<TextureAtlas>,
    pub p1_walk: Handle<SpriteSheetAnimation>,
    pub p2_walk: Handle<SpriteSheetAnimation>,
    pub p3_walk: Handle<SpriteSheetAnimation>,

    pub tile_texture: Handle<Texture>,
}

pub fn setup_sprites(
    mut commands: Commands,
    assets: Res<AssetServer>,
    mut texture_atlases: ResMut<Assets<TextureAtlas>>,
    mut animations: ResMut<Assets<SpriteSheetAnimation>>,
    mut loading: ResMut<AssetsLoading>,
) {
    let handle = assets.load("texture.png");
    loading.add(&handle);
    let mut sheet = TextureAtlas::new_empty(handle, Vec2::new(2375.0, 2410.0));

    for &[x, y, w, h] in RECTS.iter() {
        sheet.add_texture(Rect {
            min: Vec2::new(x, y),
            max: Vec2::new(x + w, y + h),
        });
    }

    let duration = Duration::from_millis(60);

    let p1_walk = animations.add(make_animation(
        &[
            Tile::P1Walk01,
            Tile::P1Walk02,
            Tile::P1Walk03,
            Tile::P1Walk04,
            Tile::P1Walk05,
            Tile::P1Walk06,
            Tile::P1Walk07,
            Tile::P1Walk08,
            Tile::P1Walk09,
            Tile::P1Walk10,
            Tile::P1Walk11,
        ],
        duration,
    ));

    let p2_walk = animations.add(make_animation(
        &[
            Tile::P2Walk01,
            Tile::P2Walk02,
            Tile::P2Walk03,
            Tile::P2Walk04,
            Tile::P2Walk05,
            Tile::P2Walk06,
            Tile::P2Walk07,
            Tile::P2Walk08,
            Tile::P2Walk09,
            Tile::P2Walk10,
            Tile::P2Walk11,
        ],
        duration,
    ));

    let p3_walk = animations.add(make_animation(
        &[
            Tile::P3Walk01,
            Tile::P3Walk02,
            Tile::P3Walk03,
            Tile::P3Walk04,
            Tile::P3Walk05,
            Tile::P3Walk06,
            Tile::P3Walk07,
            Tile::P3Walk08,
            Tile::P3Walk09,
            Tile::P3Walk10,
            Tile::P3Walk11,
        ],
        duration,
    ));

    let texture_handle = assets.load("tilesheet.png");

    commands.insert_resource(SpriteAssets {
        texture: texture_atlases.add(sheet),
        p1_walk,
        p2_walk,
        p3_walk,
        tile_texture: texture_handle,
    });
}

fn make_animation(frames: &[Tile], duration: Duration) -> SpriteSheetAnimation {
    SpriteSheetAnimation::from_frames(
        frames
            .iter()
            .map(|t| Frame {
                duration,
                index: *t as u32,
            })
            .collect(),
    )
}

